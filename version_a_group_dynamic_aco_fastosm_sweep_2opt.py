class GroupDynamicRoadTSP:
    def __init__(
        self,
        base_matrix,
        start_index=0,
        update_interval=10,
        noise_range=(0.9, 1.1),
        drop_fraction=0.05,
        dynamics_seed=123,
        inf_cost=1e12,
    ):
        self.base_matrix = base_matrix.copy()
        self.current_matrix = base_matrix.copy()
        self.start_index = start_index
        self.update_interval = update_interval
        self.noise_range = noise_range
        self.drop_fraction = drop_fraction
        self.inf_cost = inf_cost
        self.rng = np.random.default_rng(dynamics_seed)
        self.available_mask = np.ones(base_matrix.shape[0], dtype=bool)
        self.available_mask[start_index] = True

    @property
    def n(self):
        return self.base_matrix.shape[0]

    def update(self, iteration):
        if iteration == 0 or iteration % self.update_interval != 0:
            return False

        low, high = self.noise_range
        noise = self.rng.uniform(low, high, size=self.base_matrix.shape)
        self.current_matrix = self.base_matrix * noise
        np.fill_diagonal(self.current_matrix, 0.0)

        self.available_mask = np.ones(self.n, dtype=bool)
        self.available_mask[self.start_index] = True

        candidates = np.array([i for i in range(self.n) if i != self.start_index], dtype=int)
        drop_count = int(candidates.size * self.drop_fraction)

        if drop_count > 0:
            drop_indices = self.rng.choice(candidates, size=drop_count, replace=False)
            self.available_mask[drop_indices] = False

        return True

    def filter_tour(self, tour):
        return [
            i for i in tour
            if i != self.start_index and self.available_mask[i]
        ]

    def route_cost(self, tour):
        filtered = self.filter_tour(tour)
        if not filtered:
            return 0.0

        ordered = [self.start_index] + filtered + [self.start_index]
        total = 0.0

        for a, b in zip(ordered[:-1], ordered[1:]):
            cost = self.current_matrix[a, b]
            if cost >= self.inf_cost / 2:
                return self.inf_cost
            total += cost

        return float(total)


def nearest_neighbor_tour(matrix, start_index, available_mask, inf_cost):
    unvisited = np.flatnonzero(available_mask).tolist()
    if start_index in unvisited:
        unvisited.remove(start_index)

    tour = []
    current = start_index

    while unvisited:
        feasible = [i for i in unvisited if matrix[current, i] < inf_cost / 2]
        if feasible:
            nxt = min(feasible, key=lambda i: matrix[current, i])
        else:
            nxt = min(unvisited, key=lambda i: matrix[current, i])

        tour.append(nxt)
        unvisited.remove(nxt)
        current = nxt

    return tour


def run_nearest_neighbor_baseline(env, iterations, recompute_on_update=True):
    current_tour = nearest_neighbor_tour(env.current_matrix, env.start_index, env.available_mask, env.inf_cost)
    best_tour = current_tour[:]
    best_cost = env.route_cost(current_tour)

    history = [best_cost]
    updates = []

    for iteration in range(1, iterations + 1):
        updated = env.update(iteration)

        if updated:
            updates.append(iteration)
            if recompute_on_update:
                current_tour = nearest_neighbor_tour(
                    env.current_matrix,
                    env.start_index,
                    env.available_mask,
                    env.inf_cost,
                )

        cost = env.route_cost(current_tour)

        if cost < best_cost:
            best_cost = cost
            best_tour = current_tour[:]

        history.append(cost)

    return {
        "best_tour": best_tour,
        "best_cost": best_cost,
        "history": history,
        "updates": updates,
    }

"""## 7. Dynamic ACO"""

class DynamicACO:
    def __init__(
        self,
        n_nodes,
        start_index,
        alpha=1.0,
        beta=4.0,
        rho=0.30,
        event_rho=0.60,
        event_window=3,
        q=100.0,
        elite_weight=2.0,
        top_k=5,
        n_ants=None,
        seed=42,
    ):
        self.n_nodes = n_nodes
        self.start_index = start_index
        self.alpha = alpha
        self.beta = beta
        self.rho = rho
        self.event_rho = event_rho
        self.event_window = event_window
        self.q = q
        self.elite_weight = elite_weight
        self.top_k = top_k
        self.n_ants = n_ants
        self.rng = random.Random(seed)

        self.pheromone = np.ones((n_nodes, n_nodes), dtype=float)
        np.fill_diagonal(self.pheromone, 0.0)

    def choose_next_node(self, current, unvisited, matrix, inf_cost):
        weights = []

        for node in unvisited:
            distance = matrix[current, node]
            if distance >= inf_cost / 2:
                weight = 0.0
            else:
                tau = self.pheromone[current, node] ** self.alpha
                eta = (1.0 / (distance + 1e-9)) ** self.beta
                weight = tau * eta
            weights.append(weight)

        total = sum(weights)

        if total <= 0:
            feasible = [node for node in unvisited if matrix[current, node] < inf_cost / 2]
            if feasible:
                return min(feasible, key=lambda node: matrix[current, node])
            return self.rng.choice(unvisited)

        threshold = self.rng.random() * total
        cumulative = 0.0

        for node, weight in zip(unvisited, weights):
            cumulative += weight
            if cumulative >= threshold:
                return node

        return unvisited[-1]

    def construct_tour(self, matrix, available_mask, inf_cost):
        unvisited = np.flatnonzero(available_mask).tolist()
        if self.start_index in unvisited:
            unvisited.remove(self.start_index)

        tour = []
        current = self.start_index

        while unvisited:
            next_node = self.choose_next_node(current, unvisited, matrix, inf_cost)
            tour.append(next_node)
            unvisited.remove(next_node)
            current = next_node

        return tour

    def evaporate(self, rho):
        self.pheromone *= (1.0 - rho)
        np.fill_diagonal(self.pheromone, 0.0)
        self.pheromone = np.maximum(self.pheromone, 1e-12)

    def deposit(self, tour, cost, multiplier=1.0):
        if tour is None or cost <= 0:
            return
        ordered = [self.start_index] + [i for i in tour if i != self.start_index] + [self.start_index]
        delta = multiplier * self.q / cost

        for a, b in zip(ordered[:-1], ordered[1:]):
            self.pheromone[a, b] += delta
            self.pheromone[b, a] += delta

    def update_pheromone(self, tours_and_costs, best_tour, best_cost, rho, inf_cost):
        self.evaporate(rho)

        feasible = [(t, c) for t, c in tours_and_costs if c < inf_cost / 2]

        for tour, cost in sorted(feasible, key=lambda x: x[1])[: self.top_k]:
            self.deposit(tour, cost, multiplier=1.0)

        if best_tour is not None and best_cost < inf_cost / 2:
            self.deposit(best_tour, best_cost, multiplier=self.elite_weight)

class GroupDynamicACO(DynamicACO):
    def run(self, env, iterations):
        best_tour = None
        best_cost = float("inf")
        history = []
        updates = []
        event_timer = 0
        infeasible_rejected = 0

        for iteration in range(iterations + 1):
            updated = env.update(iteration)

            if updated:
                updates.append(iteration)
                event_timer = self.event_window

                if best_tour is not None:
                    best_cost = env.route_cost(best_tour)
                    if best_cost >= env.inf_cost / 2:
                        best_tour = None
                        best_cost = float("inf")

            ants = self.n_ants if self.n_ants is not None else int(env.available_mask.sum())
            tours_and_costs = []

            for _ in range(ants):
                tour = self.construct_tour(env.current_matrix, env.available_mask, env.inf_cost)
                cost = env.route_cost(tour)

                if cost >= env.inf_cost / 2:
                    infeasible_rejected += 1
                    continue

                tours_and_costs.append((tour, cost))

                if cost < best_cost:
                    best_tour = tour[:]
                    best_cost = cost

            rho_used = self.event_rho if event_timer > 0 else self.rho
            self.update_pheromone(tours_and_costs, best_tour, best_cost, rho_used, env.inf_cost)

            history.append(best_cost)

            if event_timer > 0:
                event_timer -= 1

        return {
            "best_tour": best_tour,
            "best_cost": best_cost,
            "history": history,
            "updates": updates,
            "infeasible_rejected": infeasible_rejected,
        }

