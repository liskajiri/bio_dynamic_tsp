"""## 11. Concatenated method: Dynamic ACO + 2-opt

This is a small hybrid/concatenated method:

```text
Dynamic ACO = global search
2-opt = local route refinement
```

ACO finds a good route under dynamic conditions. Then 2-opt tries to improve the final order by reversing route segments. If a reversal reduces the route cost in the final dynamic state, it is kept.
"""

def build_final_environment():
    """Recreate the same final dynamic environment used at N_ITERATIONS."""
    final_env = GroupDynamicRoadTSP(
        base_matrix=base_matrix,
        start_index=START_INDEX,
        update_interval=UPDATE_INTERVAL,
        noise_range=NOISE_RANGE,
        drop_fraction=DROP_FRACTION,
        dynamics_seed=DYNAMICS_SEED,
        inf_cost=INF_COST,
    )

    for iteration in range(1, N_ITERATIONS + 1):
        final_env.update(iteration)

    return final_env


def two_opt_improve(tour, env, max_passes=5):
    """
    Simple 2-opt improvement on the final active route.

    The tour does not include the depot. Unavailable nodes are filtered by env.
    """
    best = env.filter_tour(tour)
    best_cost = env.route_cost(best)

    if not best or best_cost >= env.inf_cost / 2:
        return best, best_cost, 0

    improvements = 0

    for _ in range(max_passes):
        improved_this_pass = False

        for i in range(len(best) - 2):
            for j in range(i + 2, len(best) + 1):
                candidate = best[:]
                candidate[i:j] = reversed(candidate[i:j])

                cost = env.route_cost(candidate)

                if cost < best_cost:
                    best = candidate
                    best_cost = cost
                    improvements += 1
                    improved_this_pass = True

        if not improved_this_pass:
            break

    return best, best_cost, improvements


final_env = build_final_environment()

